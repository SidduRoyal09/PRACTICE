package com.Practice;

public class BitwiseOp {

	public static void main(String[] args) {
		
		// & , | , ^ , << , >> , >>> , ~
		
		System.out.println(10&11);
		System.out.println(10<<2);
		System.out.println(10|11);
		System.out.println(10^11);
		System.out.println(10>>2);
		System.out.println(10>>>2);
		System.out.println(~10);
		
	}

}

