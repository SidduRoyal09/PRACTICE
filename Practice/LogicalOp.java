package com.Practice;

public class LogicalOp {

	public static void main(String[] args) {
		
		// &&-->And
		// ||-->OR
		// ! -->Not
		
		System.out.println(10<20 && 40>46);
		System.out.println(10>20 || 40<46);
		System.out.println(10<20 && 40>46);
		System.out.println(100>20 && 40<46);
		System.out.println(!(40<46));
		
		
		
	}

}
